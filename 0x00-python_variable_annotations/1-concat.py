#!/usr/bin/env python3

"""
This script defines a function that concat two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    concat two strings and return a concatenated string

    Args:
        str1 (str): the first string argument
        str2 (str): the second string argument

    Returns:
        str: a concatenated string
    """
    return str1 + str2
