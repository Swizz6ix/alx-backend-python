#!/usr/bin/env python3

"""
A function that takes a float a and a float b as argument and
return their sum as float
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers a and b

    Args:
        a (float): The first float number
        b (float): The second float number

    Returns:
        floats: The sum of a and b
    """
    return a + b
